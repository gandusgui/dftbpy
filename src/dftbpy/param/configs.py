angular_number = {"s": 0, "p": 1, "d": 2, "f": 3}
angular_name = {0: "s", 1: "p", 2: "d", 3: "f"}


def convert_configuration(*args):
    """Convert between string and tuple formats."""
    if len(args) == 1:  # string format
        args = args[0]

        n, l = int(args[0]), angular_number[args[1]]

        # format = nl
        if len(args) == 2:
            return n, l

        # format = nlf
        f = int(args[2:])
        return n, l, f

    # tuple format
    n, l = args[:2]
    nl = str(n) + angular_name[l]

    # format = (n, l)
    if len(args) == 2:
        return nl

    # format = (n, l, f)
    f = args[2]
    return nl + str(f)


